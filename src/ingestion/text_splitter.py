def split_text(text):
    chunk_size = 1000
    chunk_overlap = 200

    if not text:
        return []

    chunks = []
    start = 0
    text_len = len(text)

    while start < text_len:
        end = min(start + chunk_size, text_len)
        chunks.append(text[start:end])
        if end == text_len:
            break
        start += chunk_size - chunk_overlap

    return chunks